/**
 * Recursively replaces all string literal types with `string`,
 * allowing FR/AR translations to use different string values
 * while still matching the same structural type.
 */
type Stringify<T> = T extends string
  ? string
  : T extends readonly (infer U)[]
  ? Stringify<U>[]
  : T extends (infer U)[]
  ? Stringify<U>[]
  : T extends object
  ? { [K in keyof T]: Stringify<T[K]> }
  : T;

export type Translations = Stringify<typeof en>;
